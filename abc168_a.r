input_line <- readLines("stdin", n = 1)
X <- as.numeric(strsplit(input_line, " ")[[1]])
a <- X %% 10

if (a %in% c(2, 4, 5, 7, 9)) {
    cat("hon", "\n")
} else if (a %in% c(0, 1, 6, 8)) {
    cat("pon", "\n")    
} else {
    cat("bon", "\n")     
}
