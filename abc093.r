input <- readLines("stdin", n=1)
input <- tolower(input)
words <- strsplit(input, "")[[1]]
check_chars <- c("a", "b", "c")
all_found <- all(check_chars %in% words)
if (all_found) {
  cat("Yes\n")
} else {
  cat("No\n")
}
