options(scipen = 10000)
library(magrittr)
line <- readLines("stdin")
r <- line[1] %>% as.integer()
ans <- if (r < 1200) {
           "ABC"
} else if (r < 2800) {
           "ARC"
} else {
           "AGC"
}
cat(ans, "\n", sep = "")
