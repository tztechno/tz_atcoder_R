input_line <- readLines("stdin", n = 1)
S <- input_line
t <- 0
for (i in seq_along(strsplit(S, "")[[1]])) {
  if (substr(S, i, i) == 'o') {
    t <- t + 1
  }
}
cat(700 + 100 * t, "\n")
